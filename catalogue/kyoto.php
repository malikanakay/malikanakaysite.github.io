            <p>Уютно устроившись среди гор Западного Хонсю (Honshu), Киото был древней столицей Японии и резиденцией Императора
                на протяжении тысячелетия (794 - 1868 г.г.). В течение этого времени Киото накопил уникальную коллекцию дворцов,
                храмов и святынь, построенных для императоров, сёгунов и монахов, и сегодня, по-праву считается одним из самых
                красивых городов страны, являющимся хранителем многих древнейших памятников архитектуры и искусства.</p>
            <p class="mb-0">Первое, что видишь, попадая в Киото - это ультрасовременный городской железнодорожный вокзал
                из стекла и стали. Киото стремится к сохранению древних традиций, но столкновение с современностью неизбежно.
                Постепенно раскрывая красоту Киото в храмах и парках, которыми, окружен центр города, и прогуливаясь по его
                динамичным молодежным кварталам, узким улочкам, по берегу реки, понимаешь, что современность только подчеркивает
                прекрасные уголки древней культуры и создает удивительную гармонию времен, делая этот город особенным.
                </p>
